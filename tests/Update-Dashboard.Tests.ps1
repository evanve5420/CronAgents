            & $script:DashboardScript -RepoRoot $successRepo -RunsRoot $successRuns -OutputPath $successOut
            $script:SuccessDash = Get-Content -LiteralPath $successOut -Raw -Encoding UTF8
        }

        It 'Shows success icon in summary' {
            $script:SuccessDash | Should -Match ([char]0x2705)
        }

        It 'Shows agent name in summary table' {
            $script:SuccessDash | Should -Match 'Code Review'
        }

        It 'Shows summary snippet in detail column' {
            $script:SuccessDash | Should -Match 'Reviewed 5 files'
        }

        It 'Includes duration in recent run section' {
            $script:SuccessDash | Should -Match '\*\*Duration:\*\*'
            $script:SuccessDash | Should -Match '3m 42s'
        }

        It 'Shows Success status in recent run section' {
            $script:SuccessDash | Should -Match '\*\*Status:\*\* Success'
        }

        It 'Includes links to feedback and session' {
            $script:SuccessDash | Should -Match '\[Feedback\]'
            $script:SuccessDash | Should -Match '\[Session\]'
        }
    }

