            $pfRepo = Join-Path $TestDrive 'processed-fb-repo'
            $pfRuns = Join-Path $pfRepo '.cronstate\runs'
            $pfOut  = Join-Path $pfRepo 'dashboard.md'
            New-Item -Path $pfRuns -ItemType Directory -Force | Out-Null

            New-TestRun -RunsRoot $pfRuns `
