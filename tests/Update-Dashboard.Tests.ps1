            New-TestRun -RunsRoot $sumTrRuns `
                        -AgentId 'sumtr-agent' `
                        -AgentName 'SumTr Agent' `
                        -Timestamp ([datetime]::UtcNow.AddMinutes(-1)) `
                        -ExitCode 0 `
