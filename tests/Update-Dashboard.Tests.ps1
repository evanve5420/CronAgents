BeforeAll {
    $repoRoot = Split-Path $PSScriptRoot -Parent
    Import-Module (Join-Path $repoRoot 'scheduler/lib/CronAgents.psd1') -Force

    $script:DashboardScript = Join-Path $repoRoot 'scheduler\Update-Dashboard.ps1'

    function New-TestRun {
        param(
            [string]$RunsRoot,
            [string]$AgentId,
            [string]$AgentName,
            [datetime]$Timestamp,
            [int]$ExitCode       = 0,
            [bool]$TimedOut      = $false,
            [int]$RetryAttempt   = 0,
            [bool]$FeedbackProcessed = $false,
            [string]$FeedbackContent,
            [string]$SummaryContent,
            [string]$Prompt = 'Test prompt for the agent run'
        )

        $tsStr = $Timestamp.ToUniversalTime().ToString('yyyyMMddTHHmmss')
        $nonce = '{0:x4}' -f (Get-Random -Maximum 65535)
        $dirName = "${tsStr}_${AgentId}_${nonce}"
        $runDir  = Join-Path $RunsRoot $dirName
        New-Item -Path $runDir -ItemType Directory -Force | Out-Null

        $startTime = $Timestamp
        $endTime   = $Timestamp.AddMinutes(3).AddSeconds(42)

        $meta = [ordered]@{
            agentId           = $AgentId
            agentName         = $AgentName
            prompt            = $Prompt
            startTime         = $startTime.ToString('yyyy-MM-ddTHH:mm:ss')
            endTime           = $endTime.ToString('yyyy-MM-ddTHH:mm:ss')
            exitCode          = $ExitCode
            timedOut          = $TimedOut
            retryAttempt      = $RetryAttempt
            feedbackProcessed = $FeedbackProcessed
        }
        $meta | ConvertTo-Json -Depth 10 | Set-Content -Path (Join-Path $runDir 'meta.json') -Encoding UTF8

        if ($FeedbackContent) {
            Set-Content -Path (Join-Path $runDir 'feedback.md') -Value $FeedbackContent -Encoding UTF8
        }
        else {
            $stub = "<!-- Feedback for agent run: $AgentId -->`n"
            Set-Content -Path (Join-Path $runDir 'feedback.md') -Value $stub -Encoding UTF8
        }

        if ($SummaryContent) {
            Set-Content -Path (Join-Path $runDir 'summary.md') -Value $SummaryContent -Encoding UTF8
        }

        return $runDir
    }
}

# ===== Dashboard generation =====

Describe 'Update-Dashboard' {
    Context 'With no runs' {
        BeforeAll {
            $emptyRepoRoot = Join-Path $TestDrive 'empty-repo'
            $emptyRunsRoot = Join-Path $emptyRepoRoot '.cronstate\runs'
