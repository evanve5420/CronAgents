            & $script:DashboardScript -RepoRoot $emptyRepoRoot -RunsRoot $emptyRunsRoot -OutputPath $emptyOutput
            $script:EmptyDashboard = Get-Content -LiteralPath $emptyOutput -Raw -Encoding UTF8
        }

        It 'Creates the dashboard file' {
            Test-Path $emptyOutput | Should -Be $true
        }

        It 'Contains the correct header' {
            $script:EmptyDashboard | Should -Match '# CronAgents Dashboard'
        }

        It 'Contains auto-generated notice' {
            $script:EmptyDashboard | Should -Match 'Auto-generated by CronAgents'
        }

